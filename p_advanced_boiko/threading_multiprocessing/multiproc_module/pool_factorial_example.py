from multiprocessing import Pool


def factorial(N):
    fact = 1
    for i in range(1, N+1):
        fact = fact * i
    return fact


if __name__ == '__main__':
    p = Pool(5)
    result = p.map(factorial, range(1, 100, 10))
    for v in result:
        print(v)

